#!/usr/bin/env python3
"""Generate the Sparsity/Skew LaTeX table (user's template) from E5 results.

Emits the exact `tabular*` template the user supplied, with a MonetDB column ADDED and
the buggy naive-sum deltas replaced by the common-success-set per-query mean +- SEM.
Hot-Path is 0% by construction (the revision injects only into query-disjoint columns;
config/query_referenced_columns.txt is the exclude universe -> verified 0 of the 101
eligible columns is query-referenced). Columns = injection universe (101 query-disjoint).

Method (matches plot_results.plot_table3's common-success-set, but per-query for the +-):
  per engine: keep queries that SUCCEED in both baseline and the variant (median over reps),
  delta_q = (t_var - t_base)/t_base*100 ; report mean +- SEM over the common query set.

Usage: gen_skew_table.py --sf 100   (falls back to whatever E5 dir exists)
Pointed at SF100 it regenerates automatically once the chain's STEP 3 lands.
"""
import argparse
import sys
from pathlib import Path

import numpy as np

REPO = Path(__file__).resolve().parents[1]
sys.path.insert(0, str(REPO))
from experiments.plot_results import _collect_e5, safe_float, ENGINE_ORDER, engine_label  # noqa: E402

# Injection universe (revision): MCV skew covers all 101 query-disjoint eligible columns
# (column_selection_fraction=1.0); NULL sparsity draws a heavy-tailed subset of the SAME
# universe; combined = union. Hot-Path is 0% for every row by construction (none of the
# 101 is referenced by any of the 107 workload queries). See config/query_referenced_columns.txt.
COLUMNS = {"sparsity_only": 101, "skew_only": 101, "combined": 101}
HOTPATH = {"sparsity_only": "0\\%", "skew_only": "0\\%", "combined": "0\\%"}
ROW_ORDER = [("sparsity_only", "(A) Sparsity only"),
             ("skew_only",     "(B) Skew only"),
             ("combined",      "(C) Combined")]
ENGINES = ["duckdb", "cedardb", "monetdb"]


def _success_map(records):
    """query_id -> median(wall_time_ms_total) over successful reps."""
    per_q = {}
    for r in records:
        if r.get("status") != "success":
            continue
        t = safe_float(r.get("wall_time_ms_total"))
        q = r.get("query_id")
        if t is not None and t > 0 and q:
            per_q.setdefault(q, []).append(t)
    return {q: float(np.median(ts)) for q, ts in per_q.items()}


def _delta(base_map, var_map):
    """(median%, bootstrap-SE%, n) of per-query relative delta over the common success set.

    Uses the MEDIAN per-query delta (not the mean): under reps=1 a single flaky query can
    swing the mean by >1000% (e.g. CedarDB query_46: 822->10406 ms = +1166%) while the typical
    query is unaffected. The median is robust to such single-query outliers and tells the true
    'selectivity-preserving -> neutral' story; the +- is a bootstrap SE of the median (seeded,
    so the table is deterministic). Outlier tails are discussed in the text, not hidden here.
    """
    common = sorted(set(base_map) & set(var_map))
    if not common:
        return None
    rel = np.array([(var_map[q] - base_map[q]) / base_map[q] * 100.0 for q in common])
    n = len(rel)
    med = float(np.median(rel))
    if n > 1:
        rng = np.random.default_rng(0)
        boot = np.median(rel[rng.integers(0, n, size=(2000, n))], axis=1)
        se = float(boot.std())
    else:
        se = 0.0
    return med, se, n


def _cell(d):
    if d is None:
        return "n/a"
    mean, sem, _ = d
    sign = "+" if mean >= 0 else "-"
    return f"${sign}{abs(mean):.1f}{{\\pm}}{sem:.1f}\\%$"


def _rows_for(e5_dir):
    """-> (rows, ns): rows[var][engine] = _delta tuple|None; ns[engine] = common-set size. (None,None) if no baseline."""
    data = _collect_e5(e5_dir)
    if "baseline" not in data:
        return None, None
    base = {e: _success_map(data["baseline"].get(e, [])) for e in ENGINES}
    rows, ns = {}, {}
    for var, _label in ROW_ORDER:
        rows[var] = {}
        for e in ENGINES:
            d = _delta(base.get(e, {}), _success_map(data.get(var, {}).get(e, [])))
            rows[var][e] = d
            if d:
                ns.setdefault(e, d[2])
    return rows, ns


def _emit_single(rows, ns, caption_sf, out):
    n_note = ", ".join(f"{engine_label(e)} $n{{=}}{ns.get(e,0)}$" for e in ENGINES if e in ns)
    L = ["% Auto-generated by gen_skew_table.py — common-success-set, per-query MEDIAN +- bootstrap-SE.",
         f"% Scale: {caption_sf}. Common-success-set sizes: {n_note}.",
         "% Hot-Path=0% by construction: injection excludes all query-referenced columns",
         "% (config/query_referenced_columns.txt); 0 of the 101 eligible cols is referenced.",
         "\\begin{table}[h]", "  \\centering", "  \\footnotesize", "  \\setlength{\\tabcolsep}{3pt}",
         (f"  \\caption{{Sparsity and skew impact ($\\Delta$ vs.\\ baseline, {caption_sf}; "
          "common-success set, median${\\pm}$bootstrap-SE).}"),
         "  \\label{tab:eval-skew-results}",
         "  \\begin{tabular*}{\\columnwidth}{@{}l@{\\extracolsep{\\fill}}rrccc@{}}",
         "    \\toprule",
         ("    \\textbf{Variant} & \\textbf{Columns} & \\textbf{Hot-Path} & "
          "\\textbf{DuckDB $\\Delta$ (\\%)} & \\textbf{CedarDB $\\Delta$ (\\%)} & "
          "\\textbf{MonetDB $\\Delta$ (\\%)} \\\\"),
         "    \\midrule"]
    for var, label in ROW_ORDER:
        cells = " & ".join(_cell(rows[var][e]) for e in ENGINES)
        L.append(f"    {label:<20} & {COLUMNS[var]} & {HOTPATH[var]} & {cells} \\\\")
    L += ["    \\bottomrule", "  \\end{tabular*}", "\\end{table}"]
    tex = "\n".join(L) + "\n"
    Path(out).write_text(tex)
    return tex


TIER_LABEL = {"low": "low", "medium": "med", "high": "high"}


def _emit_tiers(tier_rows, out):
    """tier_rows: {tier: (rows, ns)}. Grouped table: variant x tier, 3 engine columns. Columns/Hot-Path
    are constant (101 / 0%) across every row -> stated in the caption, not repeated."""
    tiers = [t for t in ("low", "medium", "high") if t in tier_rows and tier_rows[t][0]]
    L = ["% Auto-generated by gen_skew_table.py --tiers — SF10, common-success-set, MEDIAN +- bootstrap-SE.",
         "% All variants inject into the same 101 query-disjoint columns (Hot-Path 0%); the tier",
         "% (low/medium/high) sets injection INTENSITY, not which columns -> tests neutrality vs intensity.",
         "\\begin{table}[h]", "  \\centering", "  \\footnotesize", "  \\setlength{\\tabcolsep}{3pt}",
         ("  \\caption{Sparsity and skew impact across injection intensity ($\\Delta$ vs.\\ baseline, "
          "SF10; common-success set, median${\\pm}$bootstrap-SE). All tiers inject into the same 101 "
          "query-disjoint columns (Hot-Path~0\\%); the effect stays bounded as intensity rises.}"),
         "  \\label{tab:eval-skew-tiers}",
         "  \\begin{tabular*}{\\columnwidth}{@{}ll@{\\extracolsep{\\fill}}ccc@{}}",
         "    \\toprule",
         ("    \\textbf{Variant} & \\textbf{Tier} & \\textbf{DuckDB $\\Delta$ (\\%)} & "
          "\\textbf{CedarDB $\\Delta$ (\\%)} & \\textbf{MonetDB $\\Delta$ (\\%)} \\\\"),
         "    \\midrule"]
    for vi, (var, label) in enumerate(ROW_ORDER):
        for ti, tier in enumerate(tiers):
            rows, _ns = tier_rows[tier]
            cells = " & ".join(_cell(rows[var][e]) for e in ENGINES)
            vlabel = label if ti == 0 else ""
            L.append(f"    {vlabel:<20} & {TIER_LABEL[tier]:<4} & {cells} \\\\")
        if vi < len(ROW_ORDER) - 1:
            L.append("    \\midrule")
    L += ["    \\bottomrule", "  \\end{tabular*}", "\\end{table}"]
    tex = "\n".join(L) + "\n"
    Path(out).write_text(tex)
    return tex


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--sf", default="100")
    ap.add_argument("--repo", default=str(REPO))
    ap.add_argument("--out", default=str(REPO / "eab_artifact/tables/table_skew_nullity.tex"))
    ap.add_argument("--tiers", action="store_true",
                    help="Emit the low/medium/high intensity-sweep table (reads E5_low / E5 / E5_high).")
    args = ap.parse_args()
    repo = Path(args.repo)

    if args.tiers:
        tier_dirs = {"low": repo / f".reproduce/sf{args.sf}/results/E5_low",
                     "medium": repo / f".reproduce/sf{args.sf}/results/E5",
                     "high": repo / f".reproduce/sf{args.sf}/results/E5_high"}
        tier_rows = {}
        for tier, d in tier_dirs.items():
            r, n = _rows_for(d) if d.is_dir() else (None, None)
            if r:
                tier_rows[tier] = (r, n)
            else:
                print(f"  tier {tier}: no data under {d}", file=sys.stderr)
        if not tier_rows:
            print("no tier data found", file=sys.stderr)
            sys.exit(1)
        out = args.out.replace(".tex", "_tiers.tex")
        tex = _emit_tiers(tier_rows, out)
        print(tex)
        print(f"--> wrote {out}  (tiers: {', '.join(tier_rows)})", file=sys.stderr)
        return

    # single-table mode
    e5_dir = repo / f".reproduce/sf{args.sf}/results/E5"
    used_sf = args.sf
    if not e5_dir.is_dir() or not any(e5_dir.iterdir()):
        alt = repo / ".reproduce/sf10/results/E5"
        if alt.is_dir():
            e5_dir, used_sf = alt, "10"
    rows, ns = _rows_for(e5_dir)
    if rows is None:
        print(f"no E5 baseline under {e5_dir}", file=sys.stderr)
        sys.exit(1)
    caption_sf = "SF100" if used_sf == "100" else "SF10"
    tex = _emit_single(rows, ns, caption_sf, args.out)
    print(tex)
    print(f"--> wrote {args.out}  (scale used: {caption_sf})", file=sys.stderr)


if __name__ == "__main__":
    main()
